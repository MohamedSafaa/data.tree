#' data.tree hierarchical data for R
#' 
#' Hierarchical data is ubiquitous. However, no general-use tree data structure is available in R. 
#' Where tabular data has \code{data.frame}, hierarchical data is often modeled in lists of list or similar ugly constructions. 
#' The \code{data.tree} package allows building trees of hierarchical
#' data. It allows traversing the trees and run operations on nodes or collect data, which is very handy for 
#' the rapid prototyping of algorithms. The package also contains 
#' utility functions to sort, to print, and to construct a tree from a \code{data.frame} and vice versa.
#'
#' @seealso \code{\link{Node}}
#' @seealso For more details see the \code{data.tree} vignette by running: \code{vignette("data.tree")}
#' @docType package
#' @name data.tree
